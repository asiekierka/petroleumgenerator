package drceph.petrogen.common;
import net.minecraft.entity.player.EntityPlayer;
import net.minecraft.tileentity.TileEntity;
import net.minecraft.world.World;
import cpw.mods.fml.common.network.IGuiHandler;

public class CommonProxy implements IGuiHandler {
	
	public static final String ITEM_TEXTURE = "/drceph/petrogen/sprites/items.png";
	public static final String BLOCK_TEXTURE = "/drceph/petrogen/sprites/blocks.png";
	
	public void registerRenderThings() {
		
	}

	@Override
	public Object getServerGuiElement(int ID, EntityPlayer player, World world,
			int x, int y, int z) {
		TileEntity te = world.getBlockTileEntity(x, y, z);
		if (te!=null && te instanceof TileEntityPetroleumGenerator) {
			TileEntityPetroleumGenerator tepg = (TileEntityPetroleumGenerator) te;
			return new ContainerPetroleumGenerator(player.inventory, tepg);
		} else { 
			return null;
		}
	}

	@Override
	public Object getClientGuiElement(int ID, EntityPlayer player, World world,
			int x, int y, int z) {
		// TODO Auto-generated method stub
		return null;
	}

}
